    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(jTablePesquisa.getSelectedRow() == -1){
            
            JOptionPane.showMessageDialog(null, "Você precisa selecionar um Livro para fazer Isso!");
            
        } else {
            LivroDAO livroDAO = new LivroDAO();
            String id = (String) jTablePesquisa.getModel().getValueAt(jTablePesquisa.getSelectedRow(), 0);
            this.selbook = livroDAO.pesquisaPorID(parseInt(id));
            

            jTextFieldNomeLivro.setText(selbook.getNome());
            jFormattedTextFieldDataLivro.setText(selbook.getData());
            jTextFieldQualidadeLivro.setText(selbook.getQualidade());
            jTextFieldQuantidadeLivro.setText(String.valueOf(selbook.getQuantidade()));
            jTextAreaResumoLivro.setText(selbook.getResumo());
            
           
           
            
            ManipularImagem.exibirImagemLabel(selbook.getCapa(), jLabelCapa, 80, 110);
            
            
            CardLayout cl = (CardLayout) jPanelfather.getLayout();
            cl.show(jPanelfather, "alterar");}
    }//GEN-LAST:event_jButton2ActionPerformed

