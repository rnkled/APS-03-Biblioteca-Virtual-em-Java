    private void jButtonAddCapaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddCapaActionPerformed
            
        javax.swing.JFileChooser fc = new javax.swing.JFileChooser();
        int res = fc.showOpenDialog(null);

        if (res == fc.APPROVE_OPTION) {
            File arquivo = fc.getSelectedFile();

            try {
                BufferedImage imagemBanco = ManipularImagem.setImagemDimensao(arquivo.getAbsolutePath(), 210, 260);
                BufferedImage imagemPreview = ManipularImagem.setImagemDimensao(arquivo.getAbsolutePath(), 80, 110);

                jLabelCapa.setIcon(new ImageIcon(imagemPreview));
                byte[] imagemSalvar = ManipularImagem.getImgBytes(imagemBanco);
                this.selbook.setCapa(imagemSalvar);

            } catch (Exception ex) {
               JOptionPane.showMessageDialog(null, "Erro: "+ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Voce não selecionou nenhum arquivo.");

        }

        
        
    }//GEN-LAST:event_jButtonAddCapaActionPerformed

