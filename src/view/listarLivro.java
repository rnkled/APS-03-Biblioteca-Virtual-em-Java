    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(jTextFieldPesquisa.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Você precisa Digitar algo para Pesquisar!");   
        } else {
            
            List<Livro> livros = new ArrayList<Livro>();
            if(jRadioButtonID.isSelected()) {
                LivroDAO livroDAO = new LivroDAO();
                
                Livro livro = new Livro();
                
                livro = livroDAO.pesquisaPorID(parseInt(jTextFieldPesquisa.getText()));
                livros.add(livro);
            } 
            if(jRadioButtonNome.isSelected()) {
                LivroDAO livroDAO = new LivroDAO();
                livros = livroDAO.pesquisa("Nome", jTextFieldPesquisa.getText());     
            }
            if(jRadioButtonAutor.isSelected()){
                LivroDAO livroDAO = new LivroDAO();
                
                livros = livroDAO.pesquisa("Autor", jTextFieldPesquisa.getText()); 
            }
            if(jRadioButtonCat.isSelected()){
                LivroDAO livroDAO = new LivroDAO();
                
                livros = livroDAO.pesquisa("Categoria", jTextFieldPesquisa.getText());
            }
            
            DefaultTableModel valor = (DefaultTableModel) jTablePesquisa.getModel();
            valor.getDataVector().removeAllElements();
            
            int i = 0;
            while (livros.size() > i){
                valor.addRow(new Object[]{String.valueOf(livros.get(i).getId_Livro()), 
                livros.get(i).getNome(), livros.get(i).getOPT_nm_autor(), livros.get(i).getOPT_nm_categoria()});
                i++;
        }
            
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

