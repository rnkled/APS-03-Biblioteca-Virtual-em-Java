    public void iniciarTabela(){
        
        DefaultTableModel valor = (DefaultTableModel) jTablePesquisa.getModel();
        valor.getDataVector().removeAllElements();
        
        List<Livro> livros = new ArrayList<Livro>();
        LivroDAO livroDAO = new LivroDAO();
        
        livros = livroDAO.getTodos();
        valor.getDataVector().removeAllElements();

        int i = 0;
        while (livros.size() > i){
            valor.addRow(new Object[]{String.valueOf(livros.get(i).getId_Livro()), 
            livros.get(i).getNome(), livros.get(i).getOPT_nm_autor(), livros.get(i).getOPT_nm_categoria()});
            i++;
        }
        
    }

