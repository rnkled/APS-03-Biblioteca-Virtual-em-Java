    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
            
        if(jTextFieldNomeLivro.getText().isBlank() || jTextFieldQuantidadeLivro.getText().isBlank()
            ){
            
            JOptionPane.showMessageDialog(null, "Negado! Faltam Campos Essenciais a Serem Preenchidos!");
        
        }else{
            try{
            selbook.setNome(jTextFieldNomeLivro.getText());
            selbook.setQuantidade( parseInt(jTextFieldQuantidadeLivro.getText()));
            selbook.setData(jFormattedTextFieldDataLivro.getText());
            selbook.setQualidade(jTextFieldQualidadeLivro.getText());
            selbook.setResumo(jTextAreaResumoLivro.getText());

            LivroDAO livroDAO = new LivroDAO();
            livroDAO.alterar(selbook);

            JOptionPane.showMessageDialog(null, "O Livro "+selbook.getNome()+" foi Atualizado!");
            iniciarTabela();
            CardLayout cl = (CardLayout) jPanelfather.getLayout();
            cl.show(jPanelfather, "pesquisar");                         

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e+"AreaUsuario");
        }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

